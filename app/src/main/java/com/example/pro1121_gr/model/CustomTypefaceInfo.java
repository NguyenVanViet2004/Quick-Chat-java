package com.example.pro1121_gr.model;

public class CustomTypefaceInfo {
    private String typefaceName;

    public CustomTypefaceInfo() {
    }

    public CustomTypefaceInfo(String typefaceName) {
        this.typefaceName = typefaceName;
    }

    public String getTypefaceName() {
        return typefaceName;
    }

    public void setTypefaceName(String typefaceName) {
        this.typefaceName = typefaceName;
    }
}
